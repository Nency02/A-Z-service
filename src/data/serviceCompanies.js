// Function to get companies for a specific service
export const getCompaniesForService = (serviceName) => {
  return serviceCompanies[serviceName] || [];
};

// Function to get top-rated company for a service
export const getTopRatedCompany = (serviceName) => {
  const companies = getCompaniesForService(serviceName);
  if (companies.length === 0) return null;
  
  return companies.reduce((top, current) => 
    current.rating > top.rating ? current : top
  );
};

// Function to get recommended companies (top 2)
export const getRecommendedCompanies = (serviceName) => {
  const companies = getCompaniesForService(serviceName);
  return companies
    .sort((a, b) => b.rating - a.rating)
    .slice(0, 2);
};
